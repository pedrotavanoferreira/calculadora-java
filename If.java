public class If {
    public static void main(String args[]) {
        int x = 0;
        if (x==0){
            System.out.println("a variável é o número 0");
        }else if (x==2){
            System.out.println("a variável é o número 2");
        }
         else {
            System.out.println("a variável não é o numero 0");
        }
    }
}
